# สร้างฟังก์ชัน
def hello(name): # ค่าที่รับมีอะไรบ้าง
    print("Hello %s" % name)


# เรียกใช้ฟังก์ชัน
hello("Sasikan")

# สร้างฟังก์ชันที่มีรีเทิร์นค่า
def area(width,height): # ค่าที่รับมีตัวแปรอะไรบ้าง
    c = print(width * height) # ให้ฟังก์ชันทำงานอะไร
    return c # ทำหน้าที่คำนวณค่าแต่ยังไม่ได้ปริ้นค่า


area(5,3)




